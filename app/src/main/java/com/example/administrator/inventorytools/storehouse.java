package com.example.administrator.inventorytools;


class StoreHouse
{
    private String name;

    public StoreHouse()
    {
        super();
    }

    public StoreHouse(String name)
    {
        super();
        this.name = name;
    }


    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}
